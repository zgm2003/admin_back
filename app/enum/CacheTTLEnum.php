<?php

namespace app\enum;

/**
 * 缓存 TTL 统一管理
 * 
 * 使用说明：
 * - 所有缓存操作统一使用此类定义的 TTL 常量
 * - 便于全局调整缓存策略，提高性能
 * - 缓存键名由各业务模块自行定义
 * 
 * 注意：
 * - 修改 TTL 后需要评估对业务的影响
 * - 永久缓存（PERMANENT）必须在数据变更时主动清除
 */
class CacheTTLEnum
{
    // ==================== 短期缓存（5分钟）====================
    
    /**
     * 验证码缓存（邮箱/手机）
     * 5分钟 - 安全考虑，不宜过长
     */
    const VERIFY_CODE = 300;
    
    /**
     * 会话统计数据
     * 5分钟 - 统计数据允许一定延迟
     */
    const SESSION_STATS = 300;
    
    // ==================== 中期缓存（30分钟）====================
    
    /**
     * Token 会话缓存（最重要）
     * 30分钟 - 用户活跃期间减少数据库查询
     * 
     * 说明：
     * - 每个 API 请求都会查询
     * - 缓存命中率直接影响数据库压力
     * - 30分钟是平衡性能和实时性的最佳值
     * - Token 刷新/登出时会主动删除缓存
     */
    const TOKEN_SESSION = 1800;
    
    /**
     * 权限按钮码缓存
     * 30分钟 - 权限数据不常变化
     * 
     * 说明：
     * - 每个需要权限验证的接口都会查询
     * - 权限修改时需要主动清除缓存
     */
    const PERMISSION_BUTTONS = 1800;
    
    // ==================== 超长期缓存（30天）====================
    
    /**
     * 单设备登录指针
     * 30天 - 与 refresh_token 过期时间一致
     * 
     * 说明：
     * - 用于单设备登录策略，指向当前允许的会话 ID
     * - 登录/刷新 token 时会更新指针
     */
    const SINGLE_SESSION_POINTER = 2592000; // 30 * 24 * 3600
    
    // ==================== 永久缓存 ====================
    
    /**
     * 永久缓存（无 TTL）
     * 适用于：权限树、地址树、字典数据等
     * 
     * 说明：
     * - 数据修改时必须主动清除缓存
     * - 使用 Cache::set($key, $value) 不传 TTL 参数
     */
    const PERMANENT = 0;
}

